def is_fizz_buzz(sequence):

    for index, element in enumerate(sequence):
        current_number = index + 1

        if current_number % (3 * 5) == 0:
            if element != "FizzBuzz":
                return False
            else:
                continue

        if current_number % 3 == 0:
            if element != "Fizz":
                return False
            else:
                continue

        if current_number % 5 == 0:
            if element != "Buzz":
                return False
            else:
                continue

        if element != current_number:
            return False

    return True

