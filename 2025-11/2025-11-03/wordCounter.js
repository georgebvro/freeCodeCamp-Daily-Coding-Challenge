function countWords(sentence) {

  return sentence
    .split(" ")
    .length;
}

console.log(countWords("Hello world"));
console.log(countWords("The quick brown fox jumps over the lazy dog."));
console.log(countWords("I like coding challenges!"));
console.log(countWords("Complete the challenge in JavaScript and Python."));
console.log(countWords("The missing semi-colon crashed the entire internet."));

// --- TEST SUITE ---

const testsText = `
1. countWords("Hello world") should return 2.
2. countWords("The quick brown fox jumps over the lazy dog.") should return 9.
3. countWords("I like coding challenges!") should return 4.
4. countWords("Complete the challenge in JavaScript and Python.") should return 7.
5. countWords("The missing semi-colon crashed the entire internet.") should return 7.
`;

const testsRegex = /(?<number>\d+)\.\s(?<functionCall>.+) should return (?<output>.+?)\.?$/gm;
const testData = [...testsText.matchAll(testsRegex).map(match => match.groups)];

function runTests(testData) {
  console.log("--------------------------");
  console.log("üß™Starting Verification...");
  console.log("--------------------------");

  let allPassed = true;
  let failCount = 0;
  
  testData.forEach(test => {
    const functionCallOutput = eval(test.functionCall);
    const testOutput = eval(test.output);
    let comparison;

    if (Array.isArray(testOutput))
      comparison = arraysEqual(functionCallOutput, testOutput);
    else
      comparison = functionCallOutput === testOutput;

    if (comparison) {
      console.log(`${test.number}.‚úÖPASS - Function Call: ${test.functionCall}`);
    }
    else {
      console.log(`${test.number}.‚ùåFAIL - Function Call: ${test.functionCall}\nExpected: ${testOutput}\nGot: ${functionCallOutput}`);
      allPassed = false;
      failCount ++;
    }
  })

  console.log("----------------------------");

  if (allPassed)
    console.log("üéâSUCCESS: All tests PASSED.");
  else
    console.log(`‚ö†Ô∏èWARNING: ${failCount}/${testData.length} tests FAILED.`);
}

function arraysEqual(a, b) {
  if (a.length !== b.length)
    return false;

  for (const index in a) {
    if (Array.isArray(a[index])) {
      if (Array.isArray(b[index])) {
        if (!arraysEqual(a[index], b[index]))
          return false;
      }
      else 
        return false;
    }

    else if (a[index] !== b[index])
      return false;
  }

  return true;
}

runTests(testData);