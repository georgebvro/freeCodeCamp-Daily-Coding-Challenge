function findDuplicates(arr) {
  let duplicates = [];

  arr.forEach(eachValue => {
    if (arr.filter(filteredValue => filteredValue === eachValue).length >= 2)
      duplicates.push(eachValue);
  })

  return [...new Set(duplicates.sort((a, b) => a - b))];
}

