def truncate_text(text):

    return text if len(text) <= 20 else text[0:17] + "..."

# --- TEST SUITE ---

tests_text = r'''
1. truncate_text("Hello, world!") should return "Hello, world!".
2. truncate_text("This string should get truncated.") should return "This string shoul...".
3. truncate_text("Exactly twenty chars") should return "Exactly twenty chars".
4. truncate_text(".....................") should return "....................".
'''

import re

tests_regex = re.compile(r"(?P<number>\d+)\.\s(?P<function_call>.+) should return (?P<output>.+?)\.?$", re.MULTILINE)
test_data = [item.groupdict() for item in re.finditer(tests_regex, tests_text)]

def run_tests(test_data):
    print("--------------------------")
    print("ğŸ§ª Starting Verification...")
    print("--------------------------")

    fail_count = 0

    for test in test_data:
        function_call_output = eval(test['function_call'])
        test_output = eval(test['output'])
        
        if function_call_output == test_output:
            print(f"{test['number']}.âœ… PASS - Function Call:\n{test['function_call']}")
        else:
            print(f"{test['number']}.âŒ FAIL - Function Call:\n{test['function_call']}\nExpected:\n{test_output}\nGot:\n{function_call_output}")
            fail_count += 1

    print("----------------------------",
        f"\nâš ï¸ WARNING: {fail_count}/{len(test_data)} tests FAILED." if fail_count
        else "\nğŸ‰ SUCCESS: All tests PASSED."
    )

run_tests(test_data)